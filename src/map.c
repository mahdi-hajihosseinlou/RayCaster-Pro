void SaveMap(const char* customMap) {
    FILE* file = fopen(customMap, "w");
    if (file) {
        for (int y = 0; y < MAP_HEIGHT; y++) {
            for (int x = 0; x < MAP_WIDTH; x++) {
                fprintf(file, "%d ", map[y][x]);
            }
            fprintf(file, "\n");
        }
        fclose(file);
    }
}

void LoadMap(const char* customMap) {
    FILE* file = fopen(customMap, "r");
    if (file) {
        for (int y = 0; y < MAP_HEIGHT; y++) {
            for (int x = 0; x < MAP_WIDTH; x++) {
                fscanf(file, "%d ", &map[y][x]);
            }
        }
        fclose(file);
    }
}